""" Pide al usuario que ingrese una frase. Luego, utiliza los métodos upper() y lower() para
imprimir la frase en mayúsculas y minúsculas, respectivamente. """

frase = input('Ingresa una frase: ')

print(f'La frase en mayusculas es: {frase.upper()}')
print(f'La frase en minusculas es: {frase.lower()}')